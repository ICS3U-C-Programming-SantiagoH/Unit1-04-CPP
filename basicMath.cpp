#include <cmath>
#include <iostream>


int main() {
    std::cout << "9.2 + 2.6 = " << 9.2 + 2.6 << std::endl;
    std::cout << "7.4 - 3.8 = " << 7.4 - 3.8 << std::endl;
    std::cout << "10.4 / 2.9 = " << 10.4 / 2.9 << std::endl;
    std::cout << "8.4 - 4.3 / 2 = " << 8.4 - 4.3 / 2 << std::endl;
    std::cout << "2.2^3 = " << pow(2.2, 3) << std::endl;
    std::cout << "1.7 / 3 = " << float(1.7) / float(3) << std::endl;
}
