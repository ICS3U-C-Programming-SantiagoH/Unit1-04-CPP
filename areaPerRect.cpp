// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 5cm X 3cm.
#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 3.8cm and width 7.5cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 3.8 * 7.5 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (3.8 + 7.5) << " cm\n";
}
